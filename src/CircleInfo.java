public class CircleInfo {
    private int i;
    private int j;
    private int radi;
    CircleInfo(int i, int j, int radi){
        this.i = i;
        this.j = j;
        this.radi = radi;
    }
    public Integer getI(){
        return i;
    }
    public Integer getJ(){
        return j;
    }
    public Integer getRadi(){
        return radi;
    }
}
